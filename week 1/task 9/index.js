/**
 * Task 9 - working with numbers and strings
 * Create a program that declares variables with different data types: a number (age), a string (name), and a boolean (isStudent).
 * Perform operations like concatenating the name with a greeting, converting the age to a string, and combining the boolean value with a string to form a sentence.
 * Print the results.
 */

// Variable declaration with different data types
let age = 22;
let name = "Jay";
let isStudent = true;

// Concatenating the name with a greeting
let greeting = "Hello, " + name;

// Converting the age to a string
let ageString = age.toString();

// Combining the boolean value with a string to form a sentence
let studentStatus = "Is a student: " + isStudent;

// Printing the results
console.log(greeting); // Output: Hello, Jay
console.log(ageString); // Output: 22
console.log(studentStatus); // Output: Is a student: true
